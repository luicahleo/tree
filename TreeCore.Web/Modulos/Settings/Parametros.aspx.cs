        [DirectMethod()]
        public DirectResponse AgregarEditar(bool bAgregar)
        {
            DirectResponse direct = new DirectResponse();
            ParametrosController cParametros = new ParametrosController();

            try
            {
                if (!bAgregar)
                {
                    long lS = long.Parse(GridRowSelect.SelectedRecordID);

                    Data.Parametros oDato;
                    oDato = cParametros.GetItem(lS);

                    if (oDato.Parametro == txtParametro.Text)
                    {
                        oDato.Parametro = txtParametro.Text;
                        oDato.Valor = txtValor.Text;

                    }
                    else
                    {
                        if (cParametros.RegistroDuplicado(txtParametro.Text))
                        {
                            log.Warn(GetGlobalResource(Comun.LogRegistroExistente));
                            MensajeBox(GetGlobalResource(Comun.jsInfo), GetGlobalResource(Comun.jsYaExiste), Ext.Net.MessageBox.Icon.INFO, null);
                        }
                        else
                        {
                            if (txtParametro.Text.ToUpper() == "SHARING_PARALELO")
                            {
                                oDato.Parametro = "SHARING_PARALELO";
                            }
                            else
                            {
                                oDato.Parametro = txtParametro.Text;
                            }

                            oDato.Valor = txtValor.Text;
                        }
                        
                    }
                    if (cParametros.UpdateItem(oDato))
                    {
                        log.Warn(GetGlobalResource(Comun.LogActualizacionRealizada));
                        storePrincipal.DataBind();
                    }
                }
                else
                {
                    if (cParametros.RegistroDuplicado(txtParametro.Text))
                    {
                        log.Warn(GetGlobalResource(Comun.LogRegistroExistente));
                        MensajeBox(GetGlobalResource(Comun.jsInfo), GetGlobalResource(Comun.jsYaExiste), Ext.Net.MessageBox.Icon.INFO, null);
                    }
                    else
                    {
                        Data.Parametros oDato = new Data.Parametros();

                        if (txtParametro.Text.ToUpper() == "SHARING_PARALELO")
                        {
                            oDato.Parametro = "SHARING_PARALELO";
                        }
                        else
                        {
                            oDato.Parametro = txtParametro.Text;
                        }

                        oDato.Valor = txtValor.Text;

                        if (cParametros.AddItem(oDato) != null)
                        {
                            log.Info(GetGlobalResource(Comun.LogAgregacionRealizada));
                            storePrincipal.DataBind();
                        }
                    }
                }
            }
            catch (Exception ex)
            {
                direct.Success = false;
                direct.Result = GetGlobalResource(Comun.strMensajeGenerico);
                log.Error(ex.Message);
                return direct;
            }

            direct.Success = true;
            direct.Result = "";

            return direct;
        }

        [DirectMethod()]
        public DirectResponse MostrarEditar()
        {
            DirectResponse direct = new DirectResponse();
            ParametrosController cParametros = new ParametrosController();

            try
            {
                long lS = long.Parse(GridRowSelect.SelectedRecordID);

                Data.Parametros oDato;
                oDato = cParametros.GetItem(lS);

                txtParametro.Text = oDato.Parametro;
                txtValor.Text = oDato.Valor;

                winGestion.Show();
            }
            catch (Exception ex)
            {
                direct.Success = false;
                direct.Result = GetGlobalResource(Comun.strMensajeGenerico);
                log.Error(ex.Message);
                return direct;
            }

            direct.Success = true;
            direct.Result = "";

            return direct;
        }

        [DirectMethod()]
        public DirectResponse Eliminar()
        {
            DirectResponse direct = new DirectResponse();
            ParametrosController cParametros = new ParametrosController();

            long lID = long.Parse(GridRowSelect.SelectedRecordID);

            try
            {
                if (cParametros.DeleteItem(lID))
                {
                    log.Info(GetGlobalResource(Comun.LogEliminacionRealizada));
                    direct.Success = true;
                    direct.Result = "";
                }
            }
            catch (Exception ex)
            {
                if (ex is SqlException Sql)
                {
                    direct.Success = false;
                    direct.Result = GetGlobalResource(Comun.jsTieneRegistros);
                    log.Error(Sql.Message);
                    return direct;
                }
                else
                {
                    direct.Success = false;
                    direct.Result = GetGlobalResource(Comun.strMensajeGenerico);
                    log.Error(ex.Message);
                    return direct;
                }
            }

            return direct;
        }

